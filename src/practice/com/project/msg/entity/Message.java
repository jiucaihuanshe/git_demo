package practice.com.project.msg.entity;
/** 实体对象：实现与msg表中数据映射 */
public class Message {
	private int id;
	private String content;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
